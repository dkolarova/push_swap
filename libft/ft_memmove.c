#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	char	*c_src;
	char	*c_dst;
	size_t	i;

	if (!dst && !src)
		return (NULL);
	c_src = (char *) src;
	c_dst = (char *) dst;
	i = 0;
	if (c_dst > c_src)
		while (n-- > 0)
			c_dst[n] = c_src[n];
	else
	{
		while (i < n)
		{
			c_dst[i] = c_src[i];
			i++;
		}
	}
	return (dst);
}
